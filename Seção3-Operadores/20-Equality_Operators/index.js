 /* Operadores de igualdade */

 //Igualdade estrita, valor e tipo. Garante que os dois operandos
//sejam do mesmo tipo e o mesmo valor
 console.log(1 === 1); //true
 console.log('1' === 1); //false, string e numero não são iguais

 // Lose equality
 console.log(1 == 1); //verdadeiro
 console.log(1 == '1'); //verdadeiro também, só compara o valor não o tipo
 console.log(true == 1);
