/*Operadore aritiméticos

*/

let x = 10;
let y = 3;

/*(x + y) é uma expressão
Express é algo que produz um valor */

//Adição
console.log(x + y);

//Subtração
console.log(x - y);

//Multiplicação
console.log(x * y);

//Divisão
console.log(x / y);

//Resto da divisão
/*Digamos 10 % 3 = ?
Quantas vezes podemos multiplicar 3 para chegar em 10 ou próximo?
1 x 3 = 3 ✔️ assim ficamos com resto 7;
2 x 3 = 6 ✔️ assim ficamos com resto 4
3 x 3 = 9 ✔️ assim ficamos com resto 1
4 x 3 = 12 ❌ passou

Então 10 % 3 = 1. Pois 3 x 3 = 9. E o resto da divisão é 1!

*/
console.log(x % y);


//Exponenciação
console.log(x ** y);


//Operadores de INCREMENTO E DECREMENTO
// variavel++, ++variavel
// variavel--, --variavel

//Incremento e pré incremento
console.log(x++);//Significa "Se lembre do valor original, então incremente a variável x = x +1"

console.log(++x)//Antes de atribuir o valor da variável, o valor é incrementado em 1

