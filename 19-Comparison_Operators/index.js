/*Operadores de comparação*/

 let x = 1;

 /*
 São operadores lógicos com saída true ou false
    > maior que
    >= maior ou igual a
    < menor que
    <= menor ou igual que */

console.log(x > 1); 
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);

/* Operadores de igualdade
    == verifica apenas o valor
    === verifica se os dois operandos são iguais, considerando seu tipo e valor
    !== verifica se é diferente 
    !=== verifica se o valor e tipo são diferentes ao mesmo tempo */

console.log(x === 1);
console.log(x !== 1);
