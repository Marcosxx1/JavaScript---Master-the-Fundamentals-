//função 'oi' que recebe um PARâMETRO nome
function oi (nome, sobrNome) {
    console.log(`Olá ${nome} ${sobrNome}`);

//ou console.log('Olá' + nome);
}

//chamando a função passando um ARGUMENTO
oi('Marcos','Alex');
oi('Alex', 'S');

/*Marcos é um ARGUMENTO passado para a função
'nome' é um PARAMETRO que a função recebe
*/